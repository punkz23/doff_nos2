    <li>
        Chemicals and Liquid Cargoes
        <ul>
            <li>
                All shipments having a consistency like that of water and oil and can flow freely when poured out from its containers such as thinners, paints, cream, lotion, liquid soap, shampoo, and the like.
                <ul>
                    <li>All liquid cargoes should be stored in a leak-free originally sealed containers leaving an expansion space for variations in temperature.</li>
                    <li>These should be packed in a strong wooden box or crate and use "This side up" labels. If not properly packed, chemicals and liquid cargoes could pop up and might damage the surrounding cargoes inside the van.</li>
                </ul>
            </li>
        </ul>
    </li>
    <li>
        Appliance, Gadgets, and Electronics Devices
        <ul>
            <li>
                Any apparatus or device powered by electricity and other energy input such as stove, microwave, toaster, refrigerator, and the like. Appliances with glass parts like television will be considered as breakable cargoes.
                <ul>
                    <li>Cushion edges of appliances to avoid dents and use durable boxes/packaging materials</li>
                    <li>Use bubble wrap, foam pillows, or another padding to fill gaps in the box and prevent movement while in transit. Ensure boxes are tightly sealed with durable packaging tape and likewise necessary to prevent electrical shock.</li>
                </ul>
            </li>
        </ul>
    </li>
