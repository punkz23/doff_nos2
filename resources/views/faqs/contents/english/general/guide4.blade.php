                        <ul>
                            <li>
                                Fragile cargoes should be properly packed and crated in such a way that would prevent the movement of items and eradicate the risk of breakages while in transit. - Waiver of Damages
                            </li>
                        </ul>
                    </li>
                </ul>
            </li>
